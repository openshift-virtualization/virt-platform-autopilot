// rbac-gen scans the assets/ directory and writes config/rbac/role.yaml with all
// ClusterRole rules required by virt-platform-autopilot.  Run via 'make generate-rbac'.
package main

import (
	"flag"
	"fmt"
	"os"
	"strings"

	"github.com/kubevirt/virt-platform-autopilot/pkg/rbac"
)

const outputFile = "config/rbac/role.yaml"

func main() {
	dryRun := flag.Bool("dry-run", false, "Print generated RBAC to stdout instead of writing to file")
	flag.Parse()

	if !*dryRun {
		fmt.Println("Scanning assets for resources...")
	}

	assetFS := os.DirFS("assets")
	staticRules := rbac.StaticRules()

	transitiveRules, err := rbac.TransitiveRules(assetFS)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error scanning role assets: %v\n", err)
		os.Exit(1)
	}

	dynamicRules, err := rbac.DynamicRules(assetFS)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Error scanning assets: %v\n", err)
		os.Exit(1)
	}

	if !*dryRun {
		fmt.Printf("Found %d unique resource types\n", len(dynamicRules))
		fmt.Printf("Generated %d dynamic RBAC rules\n", len(dynamicRules))
	}

	header := `# AUTO-GENERATED by 'make generate-rbac'
# DO NOT EDIT MANUALLY - your changes will be overwritten
# To modify RBAC:
#   1. Add/remove assets in assets/ directory
#   2. Run 'make generate-rbac'
#   3. Commit the updated config/rbac/role.yaml
apiVersion: rbac.authorization.k8s.io/v1
kind: ClusterRole
metadata:
  name: virt-platform-autopilot-role
rules:
`

	rulesYAML := formatRulesWithComments(staticRules, transitiveRules, dynamicRules)
	output := header + rulesYAML

	if *dryRun {
		fmt.Print(output)
		return
	}

	if err := os.WriteFile(outputFile, []byte(output), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "Error writing to %s: %v\n", outputFile, err)
		os.Exit(1)
	}

	total := len(staticRules) + len(transitiveRules) + len(dynamicRules)
	fmt.Printf("✓ RBAC ClusterRole written to %s\n", outputFile)
	fmt.Printf("  Total rules: %d (%d static + %d transitive + %d dynamic)\n", total, len(staticRules), len(transitiveRules), len(dynamicRules))
}

// formatRulesWithComments renders all rules as YAML with human-readable section comments.
func formatRulesWithComments(static, transitive, dynamic []rbac.Rule) string {
	var b strings.Builder

	b.WriteString("  # ========================================\n")
	b.WriteString("  # Operator Infrastructure (Static)\n")
	b.WriteString("  # ========================================\n")
	staticComments := []string{
		"Nodes (for hardware detection)",
		"Events (for observability - legacy core/v1 API)",
		"Events (for observability - modern events.k8s.io/v1 API)",
		"Leader Election",
		"CRD Discovery (for soft dependency detection and template introspection)",
		"OpenShift Infrastructure CR (for topology detection: HCP, compact, cloud provider)",
	}
	for i, rule := range static {
		if i < len(staticComments) {
			fmt.Fprintf(&b, "  # %s\n", staticComments[i])
		}
		writeRule(&b, rule)
	}

	if len(transitive) > 0 {
		b.WriteString("  # ========================================\n")
		b.WriteString("  # Transitive RBAC (from managed ClusterRole/Role assets)\n")
		b.WriteString("  # ========================================\n")
		for _, rule := range transitive {
			writeRule(&b, rule)
		}
	}

	b.WriteString("  # ========================================\n")
	b.WriteString("  # Managed Resources (Dynamic - from assets/)\n")
	b.WriteString("  # ========================================\n")
	for _, rule := range dynamic {
		comment := commentForAPIGroup(rule.APIGroups[0])
		hasDelete := false
		for _, v := range rule.Verbs {
			if v == "delete" {
				hasDelete = true
				break
			}
		}
		if comment != "" {
			if hasDelete {
				fmt.Fprintf(&b, "  # %s (includes tombstone cleanup)\n", comment)
			} else {
				fmt.Fprintf(&b, "  # %s\n", comment)
			}
		}
		writeRule(&b, rule)
	}

	return b.String()
}

func writeRule(b *strings.Builder, rule rbac.Rule) {
	b.WriteString("  - apiGroups:\n")
	for _, group := range rule.APIGroups {
		if group == "" {
			b.WriteString("      - \"\"\n")
		} else {
			fmt.Fprintf(b, "      - %s\n", group)
		}
	}
	b.WriteString("    resources:\n")
	for _, r := range rule.Resources {
		fmt.Fprintf(b, "      - %s\n", r)
	}
	b.WriteString("    verbs:\n")
	for _, v := range rule.Verbs {
		fmt.Fprintf(b, "      - %s\n", v)
	}
}

func commentForAPIGroup(group string) string {
	switch group {
	case "config.openshift.io":
		return "OpenShift Infrastructure"
	case "hco.kubevirt.io":
		return "HyperConverged"
	case "machineconfiguration.openshift.io":
		return "MachineConfig & KubeletConfig"
	case "operator.openshift.io":
		return "KubeDescheduler"
	case "remediation.medik8s.io":
		return "NodeHealthCheck"
	case "self-node-remediation.medik8s.io":
		return "Self Node Remediation"
	case "fence-agents-remediation.medik8s.io":
		return "Fence Agents Remediation"
	case "forklift.konveyor.io":
		return "Migration Toolkit for Virtualization (MTV)"
	case "metallb.io":
		return "MetalLB"
	case "monitoring.coreos.com":
		return "Prometheus Alert Rules"
	case "observability.openshift.io":
		return "Cluster Observability"
	default:
		return ""
	}
}
